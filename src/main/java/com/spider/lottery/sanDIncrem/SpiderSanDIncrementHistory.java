    //  数组去重函数
    public String[] removeDupliArray(String[] array)throws Exception {
        Set<String> set = new HashSet<>();
        for(int i=0;i<array.length;i++){
            set.add(array[i]);
        }
        String[] removeDupliArray = (String[]) set.toArray(new String[set.size()]);
        return removeDupliArray;
    }


